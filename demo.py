from rag_system import RAGSystem, RAGWithLLM
import os

def main():
    print("=" * 60)
    print("ДЕМОНСТРАЦИЯ RAG-СИСТЕМЫ ДЛЯ НОРМАТИВНЫХ ДОКУМЕНТОВ")
    print("=" * 60)
    
    # Создаем систему
    print("\n[1] Инициализация системы...")
    rag = RAGSystem()
    
    # Проверяем статистику
    stats = rag.get_database_stats()
    print(f"Текущее количество блоков в базе: {stats['total_chunks']}")
    
    # Добавление документов
    print("\n[2] Добавление документов в базу...")
    
    # Создаем тестовый документ (если нет реальных)
    test_doc_path = "test_document.txt"
    if not os.path.exists(test_doc_path):
        with open(test_doc_path, 'w', encoding='utf-8') as f:
            f.write("""
            ПОЛОЖЕНИЕ О КОМАНДИРОВКАХ
            
            1. Общие положения
            1.1. Настоящее положение определяет порядок направления сотрудников в служебные командировки.
            1.2. Командировка оформляется приказом генерального директора.
            
            2. Порядок оформления
            2.1. Сотрудник подает заявление на имя руководителя не позднее чем за 3 дня до командировки.
            2.2. Заявление согласовывается с руководителем подразделения.
            2.3. На основании приказа оформляется командировочное удостоверение.
            
            3. Оплата расходов
            3.1. Суточные выплачиваются в размере 1000 рублей за каждый день командировки.
            3.2. Расходы на проезд компенсируются по фактическим затратам при предоставлении билетов.
            
            4. Отчетность
            4.1. После возвращения сотрудник предоставляет авансовый отчет в течение 3 рабочих дней.
            4.2. К отчету прилагаются проездные документы и счета за проживание.
            """)
    
    # Добавляем документ
    chunks_added = rag.add_document(test_doc_path, metadata={"category": "положения"})
    print(f"Добавлено {chunks_added} блоков")
    
    # Создаем RAG с LLM
    rag_with_llm = RAGWithLLM(rag)
    
    # Тестовые запросы
    print("\n[3] Тестирование поиска...")
    print("-" * 60)
    
    test_queries = [
        "Как оформить командировку?",
        "Какие суточные выплачиваются?",
        "В какие сроки нужно сдать отчет после командировки?"
    ]
    
    for query in test_queries:
        print(f"\n🔍 Запрос: {query}")
        print("-" * 40)
        
        # Простой поиск
        result = rag_with_llm.get_answer_simple(query, top_k=3)
        print("📄 Ответ:")
        print(result['answer'])
        
        print("\n" + "=" * 60)
    
    # Статистика после добавления
    stats = rag.get_database_stats()
    print(f"\n📊 Итоговая статистика:")
    print(f"Всего блоков в базе: {stats['total_chunks']}")
    print(f"Размерность векторов: {stats['vector_dimension']}")

if __name__ == "__main__":
    main()